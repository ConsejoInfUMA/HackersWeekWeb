<h1 class="title is-2">Acerca de la Hackers Week</h1>
<p  class="box has-background-grey-dark has-text-light" >
    La Hackers Week es un evento <b>totalmente gratuito</b>, desarrollado por el Consejo de estudiantes de la ETSI Informática de la Universidad de Málaga. Durante todo el año, la organización del evento prepara las diferentes actividades a realizar para la próxima edición, además de estar presente en los cuatro días de duración de este.</p>
<div class="box has-background-grey-dark has-text-light ">
        <p> De esta forma, la Hackers Week acaba llegando año tras año para no solo los estudiantes del centro, sino para todo interesado en el sector, novedades y tecnologías de actualidad en todo el mundo en el ámbito de la informática. Cuenta con un amplio horario lleno de conferencias y talleres, ofreciendo la posibilidad a los asistentes de poder informarse sobre temas tan distintos como la seguridad informática, programación, investigación, videojuegos e inteligencia artificial. Todo esto traído a un horario de mañana como de tarde.</p>
        <br>
        <p> Además de las actividades anteriormente mencionadas, se ponen a disposición espacios habilitados con videojuegos, juegos de mesa o juegos de rol y en los que también se realizan torneos y competiciones. </p>
</div>
